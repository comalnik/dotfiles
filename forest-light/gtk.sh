#!/usr/bin/env bash

# Set GTK theme
gsettings set org.gnome.desktop.interface gtk-theme "Everforest-Green-Dark"
gsettings set org.gnome.desktop.wm.preferences theme "Everforest-Green-Dark"

# Set font
gsettings set org.gnome.desktop.interface font-name "Maple Mono 11"

# Set cursor theme
gsettings set org.gnome.desktop.interface cursor-theme "BreezeX-RosePine-Linux"

# Set icon theme
gsettings set org.gnome.desktop.interface icon-theme "Paper"