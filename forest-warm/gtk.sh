#!/usr/bin/env bash

# Set GTK theme
gsettings set org.gnome.desktop.interface gtk-theme "gruvbox-dark-gtk"
gsettings set org.gnome.desktop.wm.preferences theme "gruvbox-dark-gtk"

# Set font
gsettings set org.gnome.desktop.interface font-name "Maple Mono NF 11"
# Set cursor theme
gsettings set org.gnome.desktop.interface cursor-theme "BreezeX-RosePine-Linux"

# Set icon theme
gsettings set org.gnome.desktop.interface icon-theme "Gruvbox"
